package App;



public class BookInformation {
	
	
	public String BooksTitle;
	public int level;
	public String state;
	public String price;
	public String For;
	
	public BookInformation() {	
	}
    public BookInformation(String BooksTitle,String price) {
		
    	this.BooksTitle=BooksTitle;
    	this.level=level;
    	this.state=state;
    	this.price=price;
    	this.For=For;
	}	
    
			

}